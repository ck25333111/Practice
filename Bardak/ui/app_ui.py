class InterceptHandler(logging.Handler):
    """ĞŸĞµÑ€ĞµÑ…Ğ²Ğ°Ñ‚Ñ‹Ğ²Ğ°ĞµÑ‚ Ğ»Ğ¾Ğ³Ğ¸ Ğ¸Ğ· ÑÑ‚Ğ°Ğ½Ğ´Ğ°Ñ€Ñ‚Ğ½Ğ¾Ğ³Ğ¾ Ğ»Ğ¾Ğ³Ğ³ĞµÑ€Ğ° Python Ğ¸ ĞºĞ¸Ğ´Ğ°ĞµÑ‚ Ğ² Loguru"""

    def emit(self, record):
        # ĞŸĞ¾Ğ»ÑƒÑ‡Ğ°ĞµĞ¼ ÑƒÑ€Ğ¾Ğ²ĞµĞ½ÑŒ Ğ»Ğ¾Ğ³Ğ¸Ñ€Ğ¾Ğ²Ğ°Ğ½Ğ¸Ñ
        level = logger.level(record.levelname).name if record.levelname in logger._core.levels else record.levelno
        # ĞŸĞµÑ€ĞµĞ´Ğ°Ñ‘Ğ¼ Ğ»Ğ¾Ğ³ Ğ² Loguru
        logger.log(level, record.getMessage())

# ĞŸĞ¾Ğ´ĞºĞ»ÑÑ‡Ğ°ĞµĞ¼ Ğ½Ğ°Ñˆ Ñ…ĞµĞ½Ğ´Ğ»ĞµÑ€ Ğº ĞºĞ¾Ñ€Ğ½ĞµĞ²Ğ¾Ğ¼Ñƒ Ğ»Ğ¾Ğ³Ğ³ĞµÑ€Ñƒ Kivy (Ğ¸ Ğ²Ğ¾Ğ¾Ğ±Ñ‰Ğµ ĞºĞ¾ Ğ²ÑĞµĞ¼)
