# Утилита для конвертации HEX в RGBA с альфой
# ────────────────────────────────────────────────────────────────

def hex_to_rgba(hex_color: str, alpha: float = 1.0) -> tuple[float, float, float, float]:
    """ Преобразует HEX-цвет в формат RGBA с заданной прозрачностью.
    :param hex_color: Цвет в формате '#RRGGBB'
    :param alpha: Прозрачность от 0.0 до 1.0
    :return: Кортеж из 4 float значений (R, G, B, A)    """
    hex_color = hex_color.lstrip('#')        # убираем символ #
    r = int(hex_color[0:2], 16) / 255.0       # красный компонент
    g = int(hex_color[2:4], 16) / 255.0       # зелёный компонент
    b = int(hex_color[4:6], 16) / 255.0       # синий компонент
    return r, g, b, alpha                     # возвращаем RGBA с альфой
